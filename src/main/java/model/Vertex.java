package model;

public class Vertex {
    public double x;
    public double y;
    double z;
   public Vertex(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public double getX() {
        return z;
    }


    public double getY() {
       return y;
    }

    public double getZ() {
       return z;
    }

    public void setZ(double z) {
        this.z = z;
    }


}
